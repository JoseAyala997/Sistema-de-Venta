    private void btnañadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnañadirActionPerformed
       
        String[] agregar = new String[11];
        if (txtidalimento.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar el Alimento");

            return;
        }
        int seleccionado = cmbtipo.getSelectedIndex();
        agregar[0] = ((String) cmbtipo.getItemAt(seleccionado));
        agregar[1] = txtobservacion.getText();
        seleccionado = cmbdia.getSelectedIndex();
        agregar[2] = ((String) cmbdia.getItemAt(seleccionado));
        seleccionado = cmbperiodo.getSelectedIndex();
        agregar[3] = ((String) cmbperiodo.getItemAt(seleccionado));
        agregar[4] = txtidalimento.getText();
        agregar[5] = txtalimento.getText();
        agregar[6] = txtcantidad.getText();
        agregar[7] = txtcaloriasl.getText();
        agregar[8] = txtproteinas.getText();
        agregar[9] = txtvitaminasl.getText();
        agregar[10] = txtmineralesl.getText();
        //        int caltotal = Integer.parseInt(txttotalcalorias.getText()) + Integer.parseInt(txtcaloriasl.getText());
        //        agregar[9] = String.valueOf(format.format(Integer.parseInt("" + caltotal)));

        modelo.addRow(agregar);

        sumarsubtotal();

        txtidalimento.setText("");
        txtalimento.setText("");
        txtcantidad.setText("");
        txtcaloriasl.setText("");
        txtproteinas.setText("");
        txtvitaminasl.setText("");
        txtmineralesl.setText("");
        txtobservacion.setText("");
        txtobservacion.requestFocus();
    }//GEN-LAST:event_btnañadirActionPerformed

    private void btnquitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnquitarActionPerformed
        int fila = tablaplantilla.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes Seleccionar un alimento");

        } else {
            int confirmacion = JOptionPane.showConfirmDialog(null, "¿Deseas Eliminar el alimento de la Lista?");
            if (JOptionPane.OK_OPTION == confirmacion) {
                modelo.removeRow(fila);

            }
        }
    }//GEN-LAST:event_btnquitarActionPerformed

