    public void vaciartabla(JTable tabla) {
        try {
            int fila = tabla.getRowCount();
            for (int i = 0; fila > i; i++) {
                frmdieta_plantilla.modelo.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    void insertar_comida() {
        vcomidas datos = new vcomidas();
        fcomida funcion = new fcomida();
        // falimento cd = new falimento();

        for (int i = 0; i < tablaplantilla.getRowCount(); i++) {

            datos.setTipo((tablaplantilla.getValueAt(i, 0).toString()));
            datos.setObservacion((tablaplantilla.getValueAt(i, 1).toString()));
            datos.setDias((tablaplantilla.getValueAt(i, 2).toString()));
            datos.setPeriodo(tablaplantilla.getValueAt(i, 3).toString());
            datos.setIdalimento(Integer.parseInt(tablaplantilla.getValueAt(i, 4).toString()));
            datos.setCaloriastotales(Integer.parseInt(txttotalcalorias.getText().replaceAll("\\.", "")));
            funcion.insertarplantilla(datos);

        }
        JOptionPane.showMessageDialog(null, "Comida generada y guardada");
        vaciartabla(tablaplantilla);
        txttotalcalorias.setText("");
    }
