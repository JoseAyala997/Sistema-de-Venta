    DecimalFormat format = new DecimalFormat("###,###.##");
    float sumatoria;
    float sumador;

    void sumarsubtotal() {
        int totalrow = tablaplantilla.getRowCount();
        totalrow -= 1;
        sumatoria = 0;
        for (int i = 0; i <= (totalrow); i++) {
            sumador = Float.valueOf(tablaplantilla.getValueAt(i, 7).toString().replaceAll("\\.", ""));
            sumatoria += sumador;
        }
        txttotalcalorias.setText(String.valueOf(format.format((sumatoria))));
        //txttotalcalorias.setText(String.valueOf((sumatoria)));
    }
