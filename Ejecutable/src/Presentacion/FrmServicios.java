    private void btninsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertarActionPerformed
        if (txtproducto.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "DEBES INGRESAR EL NOMBRE PRODUCTO");
            txtproducto.requestFocus();
            return;
        }
        if (txtcantidadstock.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "DEBES INGRESAR LA CANTIDAD STOCK");
            txtcantidadstock.requestFocus();
            return;
        }
        if (txtprecio.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "DEBES INGRESAR EL PRECIO DEL SERVICIO");
            txtprecio.requestFocus();
            return;
        }

        vservicios dts = new vservicios();
        fservicios func = new fservicios();

        dts.setNombre_servicio(txtproducto.getText());
        dts.setPrecio(Long.parseLong(txtprecio.getText()));
        dts.setUnidad(cbunidadmedida.getSelectedItem().toString());
        dts.setStock(Double.parseDouble(txtcantidadstock.getText()));

        if (accion.equals("guardar")) {
            func.insertar(dts);
            JOptionPane.showMessageDialog(this, "SERVICIO REGISTRADO CORRECTAMENTE");
            mostrar("");

        }
        if (accion.equals("Editar")) {

            dts.setIdservicios(Integer.parseInt(txtid.getText()));
            func.editar(dts);
            JOptionPane.showMessageDialog(this, "SERVICIO EDITADO CORRECTAMENTE");
            mostrar("");

        }
        cancelar();
    }//GEN-LAST:event_btninsertarActionPerformed

